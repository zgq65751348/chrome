@Service
@Slf4j
public class PaidState implements States {

    UpdateTradeState updateTradeState;

    @Autowired
    private TradeMapper tradeMapper;

    public PaidState(UpdateTradeState updateTradeState) {
        this.updateTradeState = updateTradeState;
    }

    @Override
    public void updateOrderStatus(StateParam stateParam) {
        log.info("====>已支付");
        stateParam.getTrade().setStates(stateParam.getState());
        tradeMapper.updateByPrimaryKey(stateParam.getTrade());
    }
}
